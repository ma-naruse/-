package shain;

public class Shain {
	private String shainId;

	private String shainName;

	private int shainAge;

	private String shainSex;

	private String shainPostCd;

	private String shainPrefecture;

	private String shainAddress;

	private String bushoName;

	public String getShainId() {
		return shainId;
	}

	public void setShainId(String shainId) {
		this.shainId = shainId;
	}

	public String getShainName() {
		return shainName;
	}

	public void setShainName(String shainName) {
		this.shainName = shainName;
	}

	public int getShainAge() {
		return shainAge;
	}

	public void setShainAge(int shainAge) {
		this.shainAge = shainAge;
	}

	public String getShainSex() {
		return shainSex;
	}

	public void setShainSex(String shainSex) {
		this.shainSex = shainSex;
	}

	public String getShainPostCd() {
		return shainPostCd;
	}

	public void setShainPostCd(String shainPostCd) {
		this.shainPostCd = shainPostCd;
	}

	public String getShainPrefecture() {
		return shainPrefecture;
	}

	public void setShainPrefecture(String shainPrefecture) {
		this.shainPrefecture = shainPrefecture;
	}

	public String getShainAddress() {
		return shainAddress;
	}

	public void setShainAddress(String shainAddress) {
		this.shainAddress = shainAddress;
	}

	public String getBushoName() {
		return bushoName;
	}

	public void setBushoName(String bushoName) {
		this.bushoName = bushoName;
	}
}
